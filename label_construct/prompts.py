def build_variable_extract_retry_prompt(sample: dict[str, Any]) -> str:
    return (
        build_variable_extract_prompt(sample)
        + "\n\n补充要求：你已经拿到了完整样本。"
        + " 不允许回答“Please provide ...”或“请提供 ...”。"
        + " 若 `data` 为空，请直接根据 `question` 和 `answer` 中出现的符号、统计量或判断对象抽取变量；"
        + " 若确实不存在应抽取的变量，只返回 {}。"
    )


